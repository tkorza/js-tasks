// Выведите все числа, кратные 3, из массива [2, 5, 9, 15, 0, 4].
// Методы: Используйте цикл for...of.
// Входные: [2, 5, 9, 15, 0, 4] → Результат: [9, 15, 0]

let arr = [2, 5, 9, 15, 0, 4]




for (let el of arr) {
    if (el % 3 == 0) {
        console.log(el)
    } else {
        continue;
    }
}